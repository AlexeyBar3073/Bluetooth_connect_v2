// -----------------------------------------------------------------------------
// commands.h
// Реестр команд и структура payload для шины DataRouter.
//
// Назначение:
// - Типобезопасная передача команд между модулями через шину (TOPIC_CMD)
// - Единый enum Command (13 команд) — все модули используют одинаковые ID
// - CmdPayload с union для параметров команд (экономия RAM)
//
// Поток данных:
//   Android → JSON → Protocol Task → enum Command → TOPIC_CMD → Calculator/K-Line
//
// Полный список команд (13 шт):
//   reset_trip_a    — Сброс Trip A и расхода A
//   reset_trip_b    — Сброс Trip B и расхода B
//   reset_avg       — Сброс среднего расхода
//   full_tank       — Заправка (полный бак = tank_capacity)
//   correct_odo     — Корректировка одометра
//   get_cfg         — Запрос настроек (ответ из кэша Protocol)
//   set_cfg         — Установка настроек (публикация в SettingsPack)
//   kl_get_dtc      — Чтение кодов ошибок ЭБУ
//   kl_clear_dtc    — Сброс кодов ошибок ЭБУ
//   kl_reset_adapt  — Сброс адаптации АКПП
//   kl_pump_atf     — Прокачка АКПП
//   kl_detect_proto — Автоопределение протокола К-Line
//
// -----------------------------------------------------------------------------
// ПРАВИЛА ФАЙЛА:
// -----------------------------------------------------------------------------
// ✅ Можно:
//   - Добавлять новые команды в КОНЕЦ enum Command
//   - Добавлять новые поля в union CmdPayload
//   - Добавлять новые структуры параметров внутри union
//
// ❌ Нельзя:
//   - Менять числовые значения существующих enum (сломает совместимость)
//   - Удалять существующие команды
//   - Менять размеры строковых буферов в union (сломает размер CmdPayload)
//   - Использовать String (Arduino) внутри CmdPayload (утечки памяти)
//
// ВЕРСИЯ: 5.0.0 — MAJOR: Переход на агрегированные пакеты
// -----------------------------------------------------------------------------

#ifndef COMMANDS_H
#define COMMANDS_H

#include <stdint.h>

// =============================================================================
// Command — Перечисление всех команд (13 шт)
// =============================================================================
//
// Каждая команда соответствует JSON-команде от Android:
//   {"command": "reset_trip_a", "msg_id": 1}
//
// Protocol Task парсит JSON → публикует enum Command в TOPIC_CMD.
// Модули-получатели (Calculator, K-Line) обрабатывают команду.
//
// Диапазоны:
//   0x00        — Нет команды
//   0x01–0x07   — Управление trip/топливом (Calculator)
//   0x08–0x09   — Настройки (Protocol)
//   0x0A–0x0D   — К-Line диагностика (K-Line Task)
//
enum Command : uint8_t {
    CMD_NONE            = 0x00,  // Нет команды (пустышка, не используется)

    // --- Управление trip/топливом (обрабатывает Calculator Task) ---
    CMD_RESET_TRIP_A    = 0x01,  // Сброс trip_a и fuel_trip_a в 0
    CMD_RESET_TRIP_B    = 0x02,  // Сброс trip_b и fuel_trip_b в 0
    CMD_RESET_AVG       = 0x03,  // Сброс среднего расхода текущей поездки
    CMD_FULL_TANK       = 0x04,  // Заправка: fuel_base = tank_capacity
    CMD_CORRECT_ODO     = 0x05,  // Корректировка одометра (параметр: odo_value)

    // --- Настройки (обрабатывает Protocol Task) ---
    CMD_GET_CFG         = 0x06,  // Запрос всех настроек (ответ из кэша)
    CMD_SET_CFG         = 0x07,  // Установка настроек (параметры в set_cfg)

    // --- К-Line диагностика (обрабатывает K-Line Task) ---
    CMD_KL_GET_DTC      = 0x08,  // Чтение кодов ошибок ЭБУ
    CMD_KL_CLEAR_DTC    = 0x09,  // Сброс кодов ошибок ЭБУ
    CMD_KL_RESET_ADAPT  = 0x0A,  // Сброс адаптации АКПП (TCM)
    CMD_KL_PUMP_ATF     = 0x0B,  // Прокачка жидкости АКПП
    CMD_KL_DETECT_PROTO = 0x0C,   // Принудительное автоопределение протокола

    // --- Калибровка (обрабатывает RealEngine Task) ---
    CMD_CALIBRATE_SPEED   = 0x0D   // Калибровка датчика скорости (расстояние → pulses_per_meter)
};

// =============================================================================
// CmdPayload — Структура payload команды для передачи через TOPIC_CMD
// =============================================================================
//
// Размер: 20 байт (fixed)
//
// Поля:
//   cmd     — Тип команды (enum Command)
//   msg_id  — Идентификатор из Android JSON (для привязки ответа)
//   union   — Параметры команды (только одно поле активно)
//
// Использование union:
// - Экономит RAM (все параметры делят одну память)
// - Только одно поле union используется одновременно
// - Вызывающий ДОЛЖЕН обнулить CmdPayload перед заполнением (memset = 0)
//
// Примеры:
//   CMD_CORRECT_ODO  → payload.correct_odo.odo_value = 15234.5
//   CMD_SET_CFG      → payload.set_cfg.tank = 65.0, payload.set_cfg.inj_cnt = 4
//   CMD_FULL_TANK    → payload.float_val не используется (только cmd + msg_id)
//
typedef struct {
    Command  cmd;               // Тип команды (enum Command, 1 байт)

    union {
        float  float_val;       // Одиночный float-параметр (универсальный)
        int    int_val;         // Одиночный int-параметр (универсальный)

        // --- Параметры для CMD_CORRECT_ODO ---
        struct {
            double odo_value;   // Новое значение одометра (км)
        } correct_odo;

        // --- Параметры для CMD_SET_CFG ---
        struct {
            float   tank;       // Ёмкость бака (л)
            float   inj_perf;   // Производительность форсунок (мл/мин)
            uint8_t inj_cnt;    // Количество форсунок (шт)
            float   spd_sig;    // Импульсов датчика скорости на метр (имп/м)
            uint8_t kl_proto;   // Протокол К-Line (0=не опр, 1=ISO9141...)
        } set_cfg;
    };

    uint32_t msg_id;            // Идентификатор сообщения из Android JSON (для ack_id)
} CmdPayload;

#endif // COMMANDS_H
