// -----------------------------------------------------------------------------
// packets.h
// Агрегированные пакеты данных для шины DataBus.
//
// Назначение:
// - Группировка связанных параметров в атомарные структуры
// - Исключение гонок состояний (odo/trip_a/trip_b меняются одновременно)
// - Снижение нагрузки на шину (47 топиков → 9 пакетных топиков)
// - Прямая бинарная сериализация в NVS (#pragma pack(1))
//
// Архитектура:
// Каждый пакет имеет поле version (uint8_t) для будущей расширяемости.
// При изменении структуры увеличивается version — старые данные игнорируются.
//
// Пакеты:
//   EnginePack   — FAST-телеметрия (100 мс): скорость, RPM, напряжение, статус
//   TripPack     — TRIP-телеметрия (1 сек): одометр, пробеги, расход, топливо
//   ServicePack  — SERVICE-телеметрия (1 сек): температуры, ошибки, климат
//   SettingsPack — Настройки: бак, форсунки, датчик скорости, K-Line протокол
//
// Почему #pragma pack(1):
// - Гарантирует отсутствие компиляторных отступов (padding)
// - Прямая сериализация в NVS через putBytes/getBytes без ручных смещений
// - Экономия RAM (~20-30% на каждом пакете)
// - Совместимость с Python-скриптом миграции (migrate_nvs.py)
//
// -----------------------------------------------------------------------------
// ПРАВИЛА ФАЙЛА:
// -----------------------------------------------------------------------------
// ✅ Можно:
//   - Добавлять новые поля в КОНЕЦ структуры (для обратной совместимости)
//   - Увеличивать version при добавлении полей
//   - Добавлять новые типы пакетов (новый пакет → новый топик)
//
// ❌ Нельзя:
//   - Менять порядок существующих полей (сломает NVS-совместимость)
//   - Удалять существующие поля (сломает старые прошивки)
//   - Убирать #pragma pack(push, 1) / #pragma pack(pop)
//   - Менять типы существующих полей (сломает бинарную совместимость)
//
// ВЕРСИЯ: 5.0.0 — Переход на агрегированные пакеты (MAJOR)
// -----------------------------------------------------------------------------

#ifndef PACKETS_H
#define PACKETS_H

#include <stdint.h>
#include <stdbool.h>

// =============================================================================
// EnginePack — FAST-телеметрия (публикуется каждые 100 мс)
// =============================================================================
//
// Назначение:
//   Передача данных о текущем состоянии двигателя и ходовой части.
//   Используется для отображения на OLED, отправки FAST-слоя JSON на Android,
//   и как входные данные для Calculator (TripPack).
//
// Кто публикует:
//   - Simulator Task (режим симуляции, виртуальный двигатель)
//   - Real Engine Module (реальные датчики, при REAL_ENGINE_ENABLED=1)
//
// Кто подписан:
//   - Protocol Task (сборка FAST JSON)
//   - OLED Task (отображение скорости/RPM)
//   - Calculator Task (расчёт TripPack)
//
// Периодичность: 100 мс (10 Гц)
// Размер: 26 байт
//
// Маппинг полей в JSON (Protocol Task):
//   speed     → "spd"        (int, км/ч)
//   rpm       → "rpm"        (int, об/мин)
//   voltage   → "vlt"        (float, В)
//   engine_running → "eng"   (bool)
//   parking_lights → "hl"    (bool)
//   instant_fuel → "inst"    (float, л/100км или л/ч)
//   gear      → "gear"       (int)
//   selector_pos → "sel"     (string)
//   tcc_lockup → "tcc"       (bool)
//
#pragma pack(push, 1)
typedef struct {
    uint8_t  version;           // Версия пакета (текущая: 2)
    float    speed;             // Скорость автомобиля (км/ч)
    float    rpm;               // Обороты двигателя (об/мин)
    float    voltage;           // Напряжение бортсети (В)
    bool     engine_running;    // Двигатель работает (true = запущен)
    bool     parking_lights;    // Габаритные огни включены (true = вкл)
    float    instant_fuel;      // Мгновенный расход (л/100км при движении, л/ч на холостых)
    float    distance;          // Накопленный пробег за поездку (км, сбрасывается при запуске двигателя)
    float    fuel_used;         // Накопленный расход за поездку (л, сбрасывается при запуске двигателя)
    float    fuel_level_sensor; // Остаток топлива в баке (л, рассчитан EngineModule/Simulator, округлён до 0.1)
    int8_t   gear;              // Текущая передача (-1 = задняя, 0 = нейтраль, 1..8 = передачи)
    char     selector_pos[4];   // Позиция селектора АКПП ("P","R","N","D","3","2","L")
    bool     tcc_lockup;        // Блокировка гидротрансформатора АКПП (true = заблокирован)
} EnginePack;
#pragma pack(pop)

// =============================================================================
// TripPack — TRIP-телеметрия (публикуется каждые 1000 мс)
// =============================================================================
//
// Назначение:
//   Передача накопленных параметров: пробег, расход топлива, остаток топлива.
//   Все значения рассчитываются Calculator Task по формуле:
//     current = base + accumulated (за поездку)
//
// Кто публикует:
//   - Calculator Task (расчёт на основе EnginePack + base из Storage)
//
// Кто подписан:
//   - Protocol Task (сборка TRIP JSON)
//   - Storage Task (сохранение в NVS)
//   - OLED Task (отображение расхода)
//
// Периодичность: 1000 мс (1 Гц)
// Размер: 41 байт
//
// Маппинг полей в JSON (Protocol Task):
//   odo            → "odo"          (double, км)
//   trip_a         → "trip_a"       (float, км)
//   fuel_trip_a    → "fuel_a"       (float, л)
//   trip_b         → "trip_b"       (float, км)
//   fuel_trip_b    → "fuel_b"       (float, л)
//   trip_cur       → "trip_cur"     (float, км)
//   fuel_cur       → "fuel_cur"     (float, л)
//   fuel_level     → "fuel"         (float, л)
//   avg_consumption → "avg"         (float, л/100км)
//
#pragma pack(push, 1)
typedef struct {
    uint8_t  version;           // Версия пакета (текущая: 1)
    double   odo;               // Общий пробег автомобиля (км, одометр, не сбрасывается)
    float    trip_a;            // Пробег поездки A (км, сбрасывается командой reset_trip_a)
    float    fuel_trip_a;       // Расход топлива за поездку A (л, сбрасывается с trip_a)
    float    trip_b;            // Пробег поездки B (км, сбрасывается командой reset_trip_b)
    float    fuel_trip_b;       // Расход топлива за поездку B (л, сбрасывается с trip_b)
    float    trip_cur;          // Текущий пробег поездки (км, сбрасывается при запуске двигателя)
    float    fuel_cur;          // Текущий расход поездки (л, сбрасывается при запуске двигателя)
    float    fuel_level;        // Остаток топлива в баке (л)
    float    avg_consumption;   // Средний расход за текущую поездку (л/100км)
} TripPack;
#pragma pack(pop)

// =============================================================================
// ServicePack — SERVICE-телеметрия (публикуется каждые 1000 мс)
// =============================================================================
//
// Назначение:
//   Передача сервисных данных: температуры двигателя/АКПП, коды ошибок ЭБУ,
//   данные климата и датчиков состояния.
//
// Кто публикует:
//   - K-Line Task (данные ЭБУ: coolant_temp, atf_temp, dtc)
//   - Climate Task (данные климата: interior_temp, exterior_temp, tire, washer)
//
// Кто подписан:
//   - Protocol Task (сборка SERVICE JSON)
//   - Storage Task (сохранение dtc в NVS)
//
// Периодичность: 1000 мс (1 Гц)
// Размер: 84 байта
//
// Маппинг полей в JSON (Protocol Task):
//   coolant_temp  → "t_cool"     (float, °C)
//   atf_temp      → "t_atf"      (float, °C)
//   dtc_count     → "dtc_cnt"    (int, шт)
//   dtc_codes     → "dtc"        (string, через ";")
//   interior_temp → "t_int"      (float, °C)
//   exterior_temp → "t_ext"      (float, °C)
//   tire_pressure → "tire"       (bool, true = низкое давление)
//   washer_level  → "wash"       (bool, true = низкий уровень)
//
#pragma pack(push, 1)
typedef struct {
    uint8_t  version;           // Версия пакета (текущая: 1)
    float    coolant_temp;      // Температура охлаждающей жидкости (°C, от К-Line)
    float    atf_temp;          // Температура масла АКПП (°C, от К-Line)
    uint8_t  dtc_count;         // Количество кодов ошибок ЭБУ (шт)
    char     dtc_codes[64];     // Коды ошибок через точку с запятой (напр. "P0135;P0141")
    float    interior_temp;     // Температура в салоне (°C, от Climate Task)
    float    exterior_temp;     // Температура за бортом (°C, от Climate Task)
    bool     tire_pressure;     // Низкое давление в шинах (true = предупреждение)
    bool     washer_level;      // Низкий уровень жидкости омывателя (true = предупреждение)
} ServicePack;
#pragma pack(pop)

// =============================================================================
// SettingsPack — Настройки (публикуется при старте и при изменении)
// =============================================================================
//
// Назначение:
//   Передача и сохранение настраиваемых параметров автомобиля.
//   Хранятся в NVS, загружаются при старте, отправляются на Android по get_cfg.
//
// Кто публикует:
//   - Storage Task (при старте — из NVS, при set_cfg — новые значения)
//
// Кто подписан:
//   - Protocol Task (кэш для ответа get_cfg)
//   - Simulator Task (ёмкость бака для команды full_tank)
//   - Calculator Task (ёмкость бака для full_tank при not_fuel)
//   - Storage Task (сохранение при изменении)
//
// Retain: true (новые подписчики получают кэш мгновенно)
// Размер: 19 байт
//
// Маппинг полей в JSON (Protocol Task → get_cfg ответ):
//   tank_capacity   → "tank"       (float, л)
//   injector_count  → "inj_cnt"    (int, шт)
//   injector_flow   → "inj_perf"   (float, мл/мин)
//   pulses_per_meter → "spd_sig"   (float, имп/м)
//   kline_protocol  → "kl_proto"   (int, 0=не опр, 1=ISO9141, 2=KWP2000...)
//
#pragma pack(push, 1)
typedef struct {
    uint8_t  version;           // Версия пакета (текущая: 1)
    float    tank_capacity;     // Ёмкость топливного бака (литры, по умолчанию 60.0)
    uint8_t  injector_count;    // Количество топливных форсунок (шт, по умолчанию 4)
    float    injector_flow;     // Производительность одной форсунки (мл/мин, по умолчанию 250.0)
    float    pulses_per_meter;  // Импульсов датчика скорости на 1 метр (имп/м, по умолчанию 3.0)
    uint8_t  kline_protocol;    // Протокол К-Line (0=не определён, 1=ISO 9141, 2=KWP2000 5baud,
                                //   3=KWP2000 fast, 4=ISO 14230)
} SettingsPack;
#pragma pack(pop)

#endif // PACKETS_H
