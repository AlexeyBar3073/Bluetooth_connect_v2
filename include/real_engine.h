// -----------------------------------------------------------------------------
// real_engine.h
// Реальный двигатель: RMT + PCNT (аппаратные драйверы ESP-IDF).
//
// Автомобиль: Toyota RAV4 2004 (XA20, рестайлинг)
// Двигатель: 1AZ-FE (2.0L) или 2AZ-FE (2.4L), MPI
//
// Входы:
//   - GPIO4  → форсунка (оптопара 6N137, RMT измерение длительности)
//   - GPIO13 → VSS (датчик скорости, PCNT счётчик импульсов)
//              ⚠️ Нужна защита: оптопара PC817 или делитель + стабилитрон 3.3В
//
// Архитектура:
//   - RMT (Receive Mode): аппаратное измерение длительности впрыска
//   - PCNT (Pulse Counter): аппаратный счётчик импульсов VSS
//   - Кольцевой буфер RMT: 100% точность даже при задержках CPU
//   - FreeRTOS задача: каждые 100 мс → расчёт EnginePack → publish
//
// ВЕРСИЯ: 6.3.0 — RMT + PCNT (ESP-IDF v4.4.x), Toyota RAV4 2004
// -----------------------------------------------------------------------------

#ifndef REAL_ENGINE_H
#define REAL_ENGINE_H

#include <Arduino.h>
#include <freertos/FreeRTOS.h>
#include <freertos/task.h>

// =============================================================================
// Публичный API
// =============================================================================

void realEngineStart();
void realEngineStop();
bool realEngineIsRunning();

#endif // REAL_ENGINE_H
