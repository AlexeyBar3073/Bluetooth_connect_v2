// Назначение:
// - Версия прошивки (MAJOR.MINOR.BUILD) — правила версионирования в AGENTS.md
// - Аппаратные пины (потенциометр, кнопки)
// - Параметры симулятора (макс. скорость, RPM, разгон)
// - Приоритеты FreeRTOS задач
//
// При изменении версии: обновить FW_VERSION_* и FW_VERSION_STR.
// Правила версионирования: см. AGENTS.md (раздел 🏷️ ПРАВИЛА ВЕРСИОНИРОВАНИЯ).
//
