// ПРАВИЛА ФАЙЛА:
// -----------------------------------------------------------------------------
// ✅ можно:
//   - Менять версию при каждом изменении (правила в AGENTS.md)
//   - Менять аппаратные пины
//   - Менять параметры симулятора (SPEED_MAX, RPM_*)
//   - Менять приоритеты FreeRTOS задач
//
// ❌ Нельзя:
//   - Забывать обновлять FW_VERSION_STR при изменении FW_VERSION_*
//   - Забывать обновлять AGENTS.md при изменении версии
//   - Устанавливать MAJOR=0 для продакшн-версии
//
// ВЕРСИЯ: 5.0.0 — MAJOR: Переход на агрегированные пакеты (9 вместо 47 топиков)
// -----------------------------------------------------------------------------

#ifndef APP_CONFIG_H
#define APP_CONFIG_H

// =============================================================================
// Версия прошивки
// =============================================================================
//
// Формат: MAJOR.MINOR.BUILD
//
// MAJOR (5) — Кардинальное изменение архитектуры (пакеты вместо топиков)
// MINOR (0) — Новая функциональность
// BUILD (0) — Исправление ошибок
//
#define FW_VERSION_MAJOR 5
#define FW_VERSION_MINOR 1
#define FW_VERSION_BUILD 0
#define FW_VERSION_STR   "5.1.0"
#define FW_VERSION_NOTE  "KlinePack/ClimatePack split, Protocol-only ack_id, fractional telemetry"

// =============================================================================
// Аппаратные пины
// =============================================================================

#define POTENTIOMETER_PIN   33    // Потенциометр "газа" (ADC, 0-4095)
#define BUTTON_PIN          26    // Кнопка запуска двигателя (GPIO, INPUT_PULLUP)

// =============================================================================
// Параметры симулятора
// =============================================================================

#define SPEED_MAX           220.0f   // Максимальная скорость (км/ч)
#define RPM_IDLE            750.0f   // Обороты холостого хода (об/мин)
#define RPM_MAX             6500.0f  // Максимальные обороты (об/мин)

// Время разгона 0-100 км/ч (секунды)
// Определяет скорость изменения: ΔV = SPEED_MAX / ACCELERATION_TIME км/ч за секунду
#define ACCELERATION_TIME   10.0f
#define SPEED_CHANGE_PER_SEC (SPEED_MAX / ACCELERATION_TIME)  // 22 км/ч/с

// =============================================================================
// Приоритеты FreeRTOS задач (0 = низший, 4 = высший)
// =============================================================================
//
// Распределение по приоритетам:
//   3 — Protocol (JSON-обработка команд Android)
//   2 — Simulator (физика автомобиля), Calculator (расчёт TripPack)
//   1 — Storage (NVS), OLED (дисплей), K-Line (диагностика), Climate
//   0 — Не используется (резерв)
//
#define TASK_PRIORITY_SIMULATOR  2
#define TASK_PRIORITY_CALCULATOR 2
#define TASK_PRIORITY_PROTOCOL   3
#define TASK_PRIORITY_OLED       1
#define TASK_PRIORITY_STORAGE    1
#define TASK_PRIORITY_KLINE      1
#define TASK_PRIORITY_CLIMATE    1

// =============================================================================
// Размеры стека задач FreeRTOS
// =============================================================================

#define TASK_STACK_SIZE       4096   // Стандартный размер стека (байт)
#define TASK_STACK_PROTOCOL   6144   // Protocol (ArduinoJson требует больше)
#define TASK_STACK_CLIMATE    2048   // Climate (минимальный стек)

#endif // APP_CONFIG_H
