// -----------------------------------------------------------------------------
// app_config.h
// Конфигурация проекта Bluetooth_connect_v2.
//
// Назначение:
// - Версия прошивки (MAJOR.MINOR.BUILD) — правила версионирования в AGENTS.md
// - Аппаратные пины (потенциометр, кнопки)
// - Параметры симулятора (макс. скорость, RPM, разгон)
// - Приоритеты FreeRTOS задач
//
// При изменении версии: обновить FW_VERSION_* и FW_VERSION_STR.
// Правила версионирования: см. AGENTS.md (раздел 🏷️ ПРАВИЛА ВЕРСИОНИРОВАНИЯ).
//
// -----------------------------------------------------------------------------
// ПРАВИЛА ФАЙЛА:
// -----------------------------------------------------------------------------
// ✅ можно:
//   - Менять версию при каждом изменении (правила в AGENTS.md)
//   - Менять аппаратные пины
//   - Менять параметры симулятора (SPEED_MAX, RPM_*)
//   - Менять приоритеты FreeRTOS задач
//
// ❌ Нельзя:
//   - Забывать обновлять FW_VERSION_STR при изменении FW_VERSION_*
//   - Забывать обновлять AGENTS.md при изменении версии
//   - Устанавливать MAJOR=0 для продакшн-версии
//
// ВЕРСИЯ: 6.6.0 — OTA Task (специфическая задача, msg_id/ack_id гарантия)
// -----------------------------------------------------------------------------

#ifndef APP_CONFIG_H
#define APP_CONFIG_H

// =============================================================================
// Версия прошивки
// =============================================================================
//
// Формат: MAJOR.MINOR.BUILD
//
// MAJOR (6) — DataRouter: типизированные топики, очереди у модулей
// MINOR (8) — OTA Task + BT transport оптимизация
// BUILD (21) — OTA Task запускается по запросу (не в setup), экономия RAM
//
#define FW_VERSION_MAJOR 6
#define FW_VERSION_MINOR 8
#define FW_VERSION_BUILD 21
#define FW_VERSION_STR   "6.8.21"
#define FW_VERSION_NOTE  "OTA: lazy task start, dispatch safety net"

// =============================================================================
// Аппаратные пины
// =============================================================================

#define POTENTIOMETER_PIN   33    // Потенциометр "газа" (ADC, 0-4095)
#define BUTTON_PIN          26    // Кнопка запуска двигателя (GPIO, INPUT_PULLUP)
#define BUTTON_HL_PIN       27    // Кнопка габаритных огней (GPIO, INPUT_PULLUP)

// =============================================================================
// Реальный двигатель (REAL_ENGINE_ENABLED=1)
// =============================================================================

#define INJECTOR_PIN        4     // Форсунка (оптопара, положительный фронт)
#define SHAFT_PIN           13    // Датчик выходного вала (геркон)
#define VOLTAGE_ADC_PIN     32    // ADC напряжения бортсети (опционально)

// =============================================================================
// Параметры симулятора
// =============================================================================

#define SPEED_MAX           220.0f   // Максимальная скорость (км/ч)
#define RPM_IDLE            750.0f   // Обороты холостого хода (об/мин)
#define RPM_MAX             6500.0f  // Максимальные обороты (об/мин)

// Время разгона 0-100 км/ч (секунды)
// Определяет скорость изменения: ΔV = SPEED_MAX / ACCELERATION_TIME км/ч за секунду
#define ACCELERATION_TIME   10.0f
#define SPEED_CHANGE_PER_SEC (SPEED_MAX / ACCELERATION_TIME)  // 22 км/ч/с

// =============================================================================
// Приоритеты FreeRTOS задач (0 = низший, 4 = высший)
// =============================================================================
//
// Распределение по приоритетам:
//   3 — Protocol (JSON-обработка команд Android)
//   2 — Simulator (физика автомобиля), Calculator (расчёт TripPack)
//   1 — Storage (NVS), OLED (дисплей), K-Line (диагностика), Climate
//   0 — Не используется (резерв)
//
#define TASK_PRIORITY_SIMULATOR  2
#define TASK_PRIORITY_CALCULATOR 2
#define TASK_PRIORITY_PROTOCOL   3
#define TASK_PRIORITY_OLED       1
#define TASK_PRIORITY_STORAGE    1
#define TASK_PRIORITY_KLINE      1
#define TASK_PRIORITY_CLIMATE    1

// =============================================================================
// Размеры стека задач FreeRTOS
// =============================================================================
// OTA с decodeBuf в static тратит ~600-900 байт стека.
// Protocol с ArduinoJson укладывается в 4096.
// Остальные задачи — лёгкие, 3072 достаточно.
// =============================================================================

#define TASK_STACK_SIZE       3072   // Стандарт (Sim, Calc, Storage, OLED, RealEngine)
#define TASK_STACK_PROTOCOL   6144   // Protocol (ArduinoJson + OTA обработка)
#define TASK_STACK_CLIMATE    1536   // Минимальный
#define TASK_STACK_OTA        4096   // OTA Task (Update.h + OtaChunkPack)
#define TASK_STACK_BT         4096   // BT_Transport (static txBuffer/temp)

#endif // APP_CONFIG_H
