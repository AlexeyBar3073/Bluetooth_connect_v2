// -----------------------------------------------------------------------------
// oled_task.h
// Задача вывода информации на OLED дисплей SSD1306 (128x64, I2C).
//
// Назначение:
// - Подписывается на топики DataBus: скорость, RPM, топливо, напряжение, расход
// - Обновляет дисплей каждые 200 мс (5 Гц)
// - Отображает: статус двигателя, скорость, топливо + прогресс-бар, напряжение
// - Индикатор Bluetooth в правом верхнем углу (при подключении)
//
// Аппаратная конфигурация:
// - SDA = GPIO 21, SCL = GPIO 22
// - Дисплей: SSD1306 128x64, аппаратный I2C
// -----------------------------------------------------------------------------

#ifndef OLED_TASK_H
#define OLED_TASK_H

#include <Arduino.h>
#include <freertos/FreeRTOS.h>
#include <freertos/task.h>

// =============================================================================
// Публичный API
// =============================================================================

// oledTask: FreeRTOS задача вывода на OLED дисплей.
// Подписывается на топики DataBus, обновляет экран каждые 200 мс.
void oledTask(void* parameter);

// oledStart: Запускает задачу OLED.
// Инициализирует I2C и дисплей, показывает приветственный экран.
void oledStart();

// oledStop: Останавливает задачу OLED.
void oledStop();

// oledIsRunning: Возвращает true если задача активна.
bool oledIsRunning();

// oledUpdate: Обновляет содержимое дисплея.
// Вызывается из задачи каждые 200 мс. Рисует 4 строки телеметрии + BT значок.
// Примечание: Функция internal (static в oled_task.cpp), не вызывается извне.

#endif // OLED_TASK_H
