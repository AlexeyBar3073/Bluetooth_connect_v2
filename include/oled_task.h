// -----------------------------------------------------------------------------
// oled_task.h
// Задача вывода информации на OLED дисплей SSD1306 (128x64, I2C).
//
// Назначение:
// - Подписывается на топики DataBus: скорость, RPM, топливо, напряжение, расход
// - Обновляет дисплей каждые 200 мс (5 Гц)
// - Отображает: статус двигателя, скорость, топливо + прогресс-бар, напряжение
// - Индикатор Bluetooth в правом верхнем углу (при подключении)
//
// Аппаратная конфигурация:
// - SDA = GPIO 21, SCL = GPIO 22
// - Дисплей: SSD1306 128x64, аппаратный I2C
//
// Обновление (интеграция с task_common):
// - Автоматическая обработка CMD_OTA_START с полной очисткой ресурсов
// - Единый heartbeat для мониторинга в loop()
// - Регистрация подписок для корректной отписки при завершении
// - Освобождение I2C и дисплея при OTA
// -----------------------------------------------------------------------------

#ifndef OLED_TASK_H
#define OLED_TASK_H

#if OLED_ENABLED

#include <Arduino.h>
#include <freertos/FreeRTOS.h>
#include <freertos/task.h>

// =============================================================================
// Публичный API
// =============================================================================

void oledStart();
void oledStop();
bool oledIsRunning();

#endif // OLED_ENABLED

#endif // OLED_TASK_H