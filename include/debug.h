// -----------------------------------------------------------------------------
// debug.h
// Макросы для централизованного управления отладочным выводом.
//
// Назначение:
// - Замена прямых вызовов Serial.printf/Serial.println
// - Единая точка включения/выключения отладки
// - Автоматическое удаление отладочного кода в release-сборке
//
// Использование:
//   DBG_PRINTF("[Module] Value: %d\n", value);
//   DBG_PRINTLN("[Module] Message");
//
// ВЕРСИЯ: 1.0.0
// -----------------------------------------------------------------------------

#ifndef DEBUG_H
#define DEBUG_H

// =============================================================================
// Отладочные макросы
// =============================================================================

#if DEBUG_LOG
    #define DBG_PRINTF(fmt, ...) Serial.printf(fmt, ##__VA_ARGS__)
    #define DBG_PRINTLN(msg)    Serial.println(msg)
#else
    #define DBG_PRINTF(fmt, ...) do {} while(0)
    #define DBG_PRINTLN(msg)    do {} while(0)
#endif

#endif // DEBUG_H
